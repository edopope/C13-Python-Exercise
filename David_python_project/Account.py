class account:
    def __init__(self, name, balance):
        self.name = name
        self.balance = balance

    def set_name(self, name):
        self.name = name

    def set_balance(self, balance):
        self.balance = balance
    def deposit(deposit_amount):
        if deposit_amount > 0:
            balance = deposit_amount.balance + deposit_amount

    def get_name(self):
        return self.name

    def get_balance(self):
        return self.balance
